let assert = require('assert');

for (let i = 0; i < actions.length; i++) {
  let action = actions[i];
  let keyNames = Object.keys(action);
  if (keyNames[0] === 'set_bus_vect') continue;

  if (keyNames[0] === 'change_bus_vect') {
    console.log(`action${i} = env.action_space()`);
    keyNames = Object.keys(action.change_bus_vect);

    for (let j = 0; j < keyNames.length; j++) {
      let key = keyNames[j];
      if (!!isNaN(key)) {
        continue;
      }
      const substation = key;
      //console.log(substation + '');
      const elementIdkeyNames = Object.keys(action.change_bus_vect[substation + '']);
      for (let k = 0; k < elementIdkeyNames.length; k++) {
        const elementId = elementIdkeyNames[k];
        const type = action.change_bus_vect[substation + ''][elementId + ''].type;
        //console.log('elementID : ' + elementId);
        //console.log('type : ' + type);
        if (type === 'generator') {
          console.log(`action${i}.gen_change_bus = [${elementId}]`);
        } else if (type === 'line (extremity)') {
          console.log(`action${i}.line_ex_change_bus = [${elementId}]`);
        } else if (type === 'line (origin)') {
          console.log(`action${i}.line_or_change_bus = [${elementId}]`);
        } else if (type === 'load') {
          console.log(`action${i}.load_change_bus = [${elementId}]`);
        } else {
          assert(false);
        }
      }
    }
    console.log(`actions.append(action${i})`);
    console.log('#---- END OF ACTION ---');
  } else {
    assert(keyNames[0] === 'redispatch');
    let vect = action['redispatch'];
    console.log(`action${i} = env.action_space()`);
    console.log(`action${i}.redispatch = np.array(${JSON.stringify(vect)})`);
    console.log(`actions.append(action${i})`);
    console.log('#---- END OF ACTION ---');
  }
}
